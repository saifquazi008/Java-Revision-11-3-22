package com.simplilearn.method.shop;

public class Shops {

	public long id;
	public String prodName;
	public int price;
	public String description;

	public Shops(long id, String prodName, int price, String description) {
		super();
		this.id = id;
		this.prodName = prodName;
		this.price = price;
		this.description = description;
	}

	public Shops() {
		super();
	}

}
